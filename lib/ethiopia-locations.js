export function filterLocations(list, query) {
  if (!query || !query.trim()) return list.slice(0, 15);
  const q = query.toLowerCase().trim();
  return list.filter((item) => item.toLowerCase().includes(q)).slice(0, 15);
}

export function getWoredaBudget(woreda) {
  const found = WOREDAS_BUDGETS_SAFE(woreda);
  return found?.budget || null;
}

function WOREDAS_BUDGETS_SAFE(woreda) {
  return WOREDA_BUDGETS.find((w) => w.woreda.toLowerCase() === (woreda || '').toLowerCase());
}

export function formatBudgetInput(value) {
  const num = String(value).replace(/[^\d.]/g, '');
  if (!num) return '';
  const parts = num.split('.');
  parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ',');
  return parts.length > 1 ? `${parts[0]}.${parts[1]}` : parts[0];
}

export function parseBudgetInput(value) {
  return parseFloat(String(value).replace(/,/g, '')) || 0;
}

