export function getLocationCoords(region, town, zone) {
  if (town && TOWN_COORDS[town]) return TOWN_COORDS[town];
  if (region && REGION_COORDS[region]) return REGION_COORDS[region];
  if (zone) {
    const match = Object.entries(REGION_COORDS).find(([r]) =>
      zone.toLowerCase().includes(r.toLowerCase().split(' ')[0])
    );
    if (match) return match[1];
  }
  return null;
}
